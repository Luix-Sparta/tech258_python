# Operators

x = 5
y = 1

# Arithmetic operators
result = x + y + 2
print("Let's show x + y + 2 =",result)

result = x - y - 2
print("Let's show x - y - 2 =",result)

result = x * y * 2
print("Let's show x * y * 2 =",result)

result = x / y / 2
print("Let's show x / y / 2 =",result)

# % modulo --> Remained of division
result = x % y % 2
print("Let's show x % y % 2 =",result)

result = 5 % 4
print(result) # 1 left over

# Comparison operators - >, <, ==, !=, >=, <=
